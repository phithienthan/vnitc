    public function urlKey($str, $id, $tblName)
    {
        $urlKeyStr = $this->removeTiengViet($str);
        /* change all to lowercase */
        $urlKeyStr = strtolower($urlKeyStr);
        /* remove multi space by - */
        $urlKeyStr = trim($urlKeyStr);
        $urlKeyStr = preg_replace('!\s+!', '-', $urlKeyStr);
        $urlKeyStr = htmlentities($urlKeyStr, null, 'utf-8');
        $urlKeyStr = str_replace("&nbsp;", "", $urlKeyStr);
        /* check duplicate */
        $duplicate = $this->checkDuplicate($urlKeyStr, $id, $tblName);
        $i = 1;
        while ($duplicate) {
            $urlKeyStr .= "-" . $i++;
            $duplicate = $this->checkDuplicate($urlKeyStr, $id, $tblName);
        }
        return $urlKeyStr;
    }

    public function checkDuplicate($urlKey, $id, $tblName)
    {
        $qr = 'SELECT url_key FROM ' . $tblName . ' where url_key = ' . $this->_mysql->quote($urlKey);
        $category_detail = $this->_mysql->execute_query_to_array($qr);
        if ($id == "") {
            if (count($category_detail) > 0) {
                return true;
            }
        } else {
            if (count($category_detail) > 1) {
                return true;
            }
        }
        return false;
    }

    public function rewriteDuplicate($urlKey, $id, $tblName, $field)
    {
        $qr = 'SELECT ' . $field . ' FROM ' . $tblName . ' where ' . $field . ' = ' . $this->_mysql->quote($urlKey);
        $rewrite_detail = $this->_mysql->execute_query_to_array($qr);
        if ($id == "") {
            if (count($rewrite_detail) > 0) {
                return true;
            }
        } else {
            if (count($rewrite_detail) > 1) {
                return true;
            }
        }
        return false;
    }

    public function curPageURL()
    {
        $pageURL = 'http://';
        if ($_SERVER["SERVER_PORT"] != "80") {
            $pageURL .= $_SERVER["SERVER_NAME"] . ":" . $_SERVER["SERVER_PORT"] . $_SERVER["REQUEST_URI"];
        } else {
            $pageURL .= $_SERVER["SERVER_NAME"] . $_SERVER["REQUEST_URI"];
        }
        return $pageURL;
    }

    function WriteLog($fileName, $data, $breakLine = true, $addTime = true)
    {
        $fp = fopen('logs/' . $fileName, 'a');
        if ($fp) {
            if ($breakLine) {
                if ($addTime)
                    $line = date("H:i:s, d/m/Y:  ", time()) . $data . " \n";
                else
                    $line = $data . " \n";
            }
            else {
                if ($addTime)
                    $line = date("H:i:s, d/m/Y:  ", time()) . $data;
                else
                    $line = $data;
            }
            fwrite($fp, $line);
            fclose($fp);
        }
    }

    /*
     * input '31/01/2013' 
     * output '2013-01-31'
     */

    function convertDate($date)
    {
        $dateArray = explode("-", $date);
        $result = $dateArray[2] . "-" . $dateArray[1] . "-" . $dateArray[0];
        return $result;
